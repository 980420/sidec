package gehos.comun.util.reglasMedicamentos;

public class IntervaloValidezCronicismo {
	String idEntidad;
	Integer cantDiasAntes;
	Integer cantDiasDespues;
	
	public String getIdEntidad() {
		return idEntidad;
	}
	public void setIdEntidad(String idEntidad) {
		this.idEntidad = idEntidad;
	}
	public Integer getCantDiasAntes() {
		return cantDiasAntes;
	}
	public void setCantDiasAntes(Integer cantDiasAntes) {
		this.cantDiasAntes = cantDiasAntes;
	}
	public Integer getCantDiasDespues() {
		return cantDiasDespues;
	}
	public void setCantDiasDespues(Integer cantDiasDespues) {
		this.cantDiasDespues = cantDiasDespues;
	}
}
