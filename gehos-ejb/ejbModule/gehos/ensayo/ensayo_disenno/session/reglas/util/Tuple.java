package gehos.ensayo.ensayo_disenno.session.reglas.util;

public class Tuple <A,B>
{
	A first;
	B second;
	
	public A getFirst() {
		return first;
	}
	public void setFirst(A first) {
		this.first = first;
	}
	public B getSecond() {
		return second;
	}
	public void setSecond(B second) {
		this.second = second;
	}
	
	
}
