package gehos.comun.util.reglasMedicamentos;

public class MedicamentosPermitidos {
	private Integer numMedicamentos;
	private String entidad;
	
	public Integer getNumMedicamentos() {
		return numMedicamentos;
	}

	public void setNumMedicamentos(Integer numMedicamentos) {
		this.numMedicamentos = numMedicamentos;
	}

	public String getEntidad() {
		return entidad;
	}

	public void setEntidad(String entidad) {
		this.entidad = entidad;
	}
	
}
